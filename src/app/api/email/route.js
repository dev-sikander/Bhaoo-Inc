import { NextResponse } from "next/server";
import nodemailer from "nodemailer";

export async function POST(request) {
    try {
        const { name, email, phone, services, message, pageURL } = await request.json();
        const transporter = nodemailer.createTransport({
            service: "gmail",
            host: "smtp.gmail.com",
            port: 465,
            secure: true,
            auth: {
                user: 'leadshttps:bhaooinc.com.co',
                pass: "10@Kskwoks"
            }
        });
        const mailOptions = {
            from: 'leadshttps:bhaooinc.com.co',
            to: 'leadshttps:bhaooinc.com.co',
            subject: `New Lead Generated By Bhaoo Inc`,
            html: `<table>
                <thead>
                    <tr>
                        <th>Name</th>
                        <th>Email</th>
                        <th>Phone</th>
                        <th>Services</th>
                        <th>Message</th>
                        <th>URL</th>
                    </tr>
                </thead>
                <tbody>
                    <tr>
                        <td>${name}</td>
                        <td>${email}</td>
                        <td>${phone}</td>
                        <td>${services}</td>
                        <td>${message}</td>
                        <td>${pageURL}</td>
                    </tr>
                </tbody>
            </table>`
        };
        await transporter.sendMail(mailOptions);
        return NextResponse.json({ "mesaage": "Email sent successfully", "status": 200 });
    } catch (error) {
        return NextResponse.json({ "mesaage": "Failed to sent Email", "status": 500 });
    }
}